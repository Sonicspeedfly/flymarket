package app

import (
	"encoding/json"
	"errors"
	"log"
	"net/http"
	"strconv"
)

func (s *Server) handleAutificationAccount(writer http.ResponseWriter, request *http.Request) {
	writer.Header().Set("Access-Control-Allow-Origin", "*")
	tokenParam := request.URL.Query().Get("token")
	item, err := s.accountSvc.TonkenFound(request.Context(), tokenParam)
	if err != nil {
		log.Print(err)
		http.Error(writer, http.StatusText(http.StatusInternalServerError), http.StatusInternalServerError)
		return
	}
	data, err := json.Marshal(item)
	if err != nil {
		log.Print(err)
		http.Error(writer, http.StatusText(http.StatusInternalServerError), http.StatusInternalServerError)
		return
	}
	writer.Header().Set("Content-Type", "application/json")
	_, err = writer.Write(data)
	if err != nil {
		log.Print(err)
	}
}

